"""UserPromptSubmit hook — СТОП на референс.

Проблема (повторяется раз в дни): Boris даёт задачу с готовым референсом/образцом
и прямо велит по нему делать, а я референс игнорирую и изобретаю своё, кривое.
Правило feedback_open_reference_first существует в памяти, но память меня не
останавливает. Этот хук ловит момент постановки reference-задачи и механически
подсовывает жёсткий СТОП.

Почему НЕ мешает и НЕ жрёт сессию (условие Boris):
- Это UserPromptSubmit, а не Stop-хук: он только ДОБАВЛЯЕТ контекст к промпту,
  ответ не переспрашивает и не блокирует → дублей ответов в чате быть не может.
- Печатает additionalContext ТОЛЬКО когда в промпте есть слово про референс.
  На всех остальных промптах stdout пустой → ноль лишних токенов.
- Никогда не падает: любая ошибка → тихий выход без вывода.
"""
import json
import re
import sys

# Слова/фразы, которыми Boris ставит reference-задачу.
# Узко, чтобы не ловить лишнее ("как у нас/тебя" — НЕ триггер).
TRIGGER_RE = re.compile(
    r"(референс\w*|"
    r"образц\w*|образец|за\s+образец|по\s+образц\w*|под\s+образец|"
    r"по\s+примеру|за\s+основу|возьми\s+за\s+основу|"
    r"\bшаблон\w*|"
    r"как\s+у\s+vela|как\s+у\s+вела)",
    re.IGNORECASE | re.UNICODE,
)

REMINDER = """⛔ В ЗАДАЧЕ ЕСТЬ РЕФЕРЕНС/ОБРАЗЕЦ. Правило feedback_open_reference_first — исполнять механически:

1. ПЕРВОЕ действие в ответе — открыть референс через Read (файл / URL / страница-образец). До этого не писать ни строки кода и не планировать «по памяти».
2. Скопировать СТРУКТУРУ образца: разметку, контейнеры, классы, порядок секций, паттерны. Не изобретать своё «получше».
3. Отступать от образца — ТОЛЬКО там, где Boris прямо назвал отличие (напр. UI/бренд другого проекта). Всё остальное = 1:1 как в референсе.

Изобрёл своё вместо того, что в образце — это ГЛАВНЫЙ баг, из-за которого этот хук и существует. Открыть референс ПЕРВЫМ."""


def main():
    try:
        raw = sys.stdin.buffer.read()
        payload = json.loads(raw.decode("utf-8", errors="ignore"))
    except Exception:
        sys.exit(0)

    prompt = payload.get("prompt", "")
    if not isinstance(prompt, str) or not prompt.strip():
        sys.exit(0)

    if not TRIGGER_RE.search(prompt):
        sys.exit(0)  # не reference-задача → молчим, токены не тратим

    print(json.dumps({
        "hookSpecificOutput": {
            "hookEventName": "UserPromptSubmit",
            "additionalContext": REMINDER,
        }
    }))
    sys.exit(0)


if __name__ == "__main__":
    main()
