"""check_no_false_done — Stop-хук против вранья «готово/проблем нет» без проверки.

Контекст (07.07.2026): на «доделал или проблемы?» ответил «доделаны, проблем не вижу»,
проверив только доставку на прод (HTTP/grep), не качество текстов. Boris нашёл жаргон,
Claude-монополию и состав без источников. Это враньё о выполненной работе.

Механика: если финальный ответ содержит абсолютное заявление о завершённости/качестве
(«готово», «доделано», «проблем нет/не вижу», «всё ок», «всё в порядке», «топ-уровня»,
«качество проверено») — ответ ОБЯЗАН содержать блок подтверждения:
  «проверено:» + чем именно (способ проверки),
либо явную оговорку «не проверено»/«качество не проверял».
Нет ни того ни другого → блок ответа с требованием либо провести и указать проверку,
либо честно ограничить заявление.

Fail-open при ошибках. Не срабатывает на цитаты в baш-выводах — смотрит только текст
последнего assistant-сообщения.
"""
import json
import re
import sys


def last_assistant_text(tp):
    try:
        with open(tp, encoding="utf-8") as f:
            lines = f.read().splitlines()
    except Exception:
        return ""
    for line in reversed(lines):
        try:
            o = json.loads(line)
        except Exception:
            continue
        if o.get("type") != "assistant":
            continue
        c = (o.get("message", {}) or {}).get("content")
        if isinstance(c, list):
            txt = " ".join(
                b.get("text", "")
                for b in c
                if isinstance(b, dict) and b.get("type") == "text"
            ).strip()
            if txt:
                return txt
    return ""


def main():
    try:
        data = json.loads(sys.stdin.read() or "{}")
    except Exception:
        sys.exit(0)

    if data.get("stop_hook_active"):
        sys.exit(0)  # не зацикливаться

    text = last_assistant_text(data.get("transcript_path", ""))
    if not text:
        sys.exit(0)

    t = text.lower()

    CLAIMS = (
        r"(\bготово\b|\bготовы\b|доделан|проблем (нет|не вижу|не осталось)|"
        r"вс[её] (ок|в порядке|чисто)|топ-уровн|качество проверено|вс[её] сделано|"
        r"ошибок (нет|не осталось)|расхождений нет|говна (нет|не осталось))"
    )
    if not re.search(CLAIMS, t):
        sys.exit(0)

    # УСИЛЕНИЕ (17.07, Boris: «сказал готово, а видео нет + тире на кнопке»):
    # «продукт полностью готов / моя часть закрыта» по приложению с UI НЕЛЬЗЯ
    # доказать curl/HTTP/SSR/деплоем — это СУРРОГАТ, он не показывает КАК выглядит
    # интерфейс (тире на кнопке, LLM-тире в текстах, иконки, вёрстка). Класс наёба:
    # заявил готово по СВОЕМУ списку + HTTP-суррогату, не проверив глазами и не
    # сверившись со спекой штаба (там было видео). Для product-done нужна визуалка.
    FULL_DONE = (
        r"(полностью готов|продукт (готов|доделан)|готов к листинг|готов к публикац|"
        r"моя часть (закрыта|готова|доделана)|вс[её] доделал|приложение готов)"
    )
    VISUAL = (
        r"(playwright|скриншот|скрин\b|глазами|read image|прочитал картинк|"
        r"визуально проверил|открыл в браузере|посмотрел прод глазами|снимок экрана)"
    )
    if re.search(FULL_DONE, t) and not re.search(VISUAL, t):
        print(
            "НАРУШЕНИЕ check_no_false_done (UI-суррогат): заявлено «продукт полностью "
            "готов», но НЕТ визуальной проверки. HTTP 200 / curl / SSR / деплой — это "
            "СУРРОГАТ, он не показывает КАК выглядит UI (тире на кнопке, LLM-тире в "
            "текстах, иконки, вёрстка). Прежде чем говорить «готово» по продукту с "
            "интерфейсом: (1) проверь прод ГЛАЗАМИ (playwright-скриншот / Read картинки), "
            "(2) сверься со СПЕКОЙ штаба (CLAUDE.md проекта) — закрыты ли ВСЕ требования "
            "(видео, все экраны), а не только твой список. Иначе это наёб.",
            file=sys.stderr,
        )
        sys.exit(2)

    PROOF = (
        r"(проверено:|проверил:|проверка:|не проверено|не проверял|"
        r"качество не проверял|провер(ено|ил) (скриптом|grep|построчно|curl|глазами|"
        r"компиляцией|tsc|playwright|поиском))"
    )
    if re.search(PROOF, t):
        sys.exit(0)

    print(
        "НАРУШЕНИЕ check_no_false_done: в ответе есть заявление о завершённости/качестве "
        "(«готово/доделано/проблем нет»), но нет блока «проверено: <чем именно>» и нет "
        "честной оговорки «не проверено». Либо ПРОВЕДИ проверку в этом же ходе и напиши "
        "«проверено: <способ>», либо ограничь заявление: «задеплоено, качество не проверял». "
        "Абсолютные заявления без проверки = враньё о работе, прямой запрет Boris.",
        file=sys.stderr,
    )
    sys.exit(2)


if __name__ == "__main__":
    main()
