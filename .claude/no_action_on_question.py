"""no_action_on_question — блокирующий хук против моментальных правок на ОЦЕНОЧНУЮ критику.

Что ловит: Boris даёт субъективную оценку копи/контента («хуйня», «отсебятина»,
«почему убрал?», «при чём тут X?») — а я цепляюсь за слово и сразу правлю продукт,
вместо того чтобы подумать, объяснить логику, предложить и дождаться команды.

Что НЕ должно блокироваться (чинится/делается сразу):
- баг-репорт (мелькает, пропадает, сломано, не работает, висит, лагает) — fix_bugs_immediately;
- явная команда (сделай/правь/деплой/убери/ок/давай);
- упрёк в бездействии / требование действия (почему ждёшь, не жди, где фикс, действуй);
- правки самих механизмов (.claude/*, settings*.json).

Смотрит последние до 3 сообщений Boris (он дробит мысль на несколько реплик).
Fail-open при любой ошибке.

ВАЖНО (нарушение 08.07.2026): правка продукта через python-скрипт в Bash ОБХОДИТ этот
хук. Обход разрешён ТОЛЬКО при явной команде Boris в его последнем сообщении
(«сделай/делай/правь/выровняй/убери»). Вопрос («единый формат?», «это эталон?») —
НЕ команда: сначала ответить словами, дождаться команды. Исполнил вопрос как команду
в обход хука — повторение главного запрещённого паттерна.
"""
import json
import re
import sys


def decide():
    try:
        data = json.loads(sys.stdin.read() or "{}")
    except Exception:
        return None

    if data.get("hook_event_name") != "PreToolUse":
        return None
    if data.get("tool_name") not in ("Edit", "Write", "MultiEdit", "NotebookEdit"):
        return None

    ti = data.get("tool_input", {}) or {}
    fp = str(ti.get("file_path", "")).replace("\\", "/").lower()

    # Файлы механизмов — всегда можно (чинить хуки/настройки во время критики)
    if "/.claude/" in fp or fp.endswith("settings.local.json") or fp.endswith("settings.json"):
        return None

    tp = data.get("transcript_path")
    if not tp:
        return None
    try:
        with open(tp, encoding="utf-8") as f:
            lines = f.read().splitlines()
    except Exception:
        return None

    # последние до 3 НАСТОЯЩИХ сообщений Boris (не tool_result, не служебные инжекты)
    SERVICE_MARKERS = (
        "Жесткие правила",
        "persisted-output",
        "hook additional context",
        "<local-command",
        "Caveat:",
        "<command-name>",
        "task-notification",
    )
    msgs = []
    for line in reversed(lines):
        try:
            o = json.loads(line)
        except Exception:
            continue
        if o.get("type") != "user":
            continue
        if o.get("isMeta"):
            continue
        c = (o.get("message", {}) or {}).get("content")
        text = ""
        if isinstance(c, str):
            text = c
        elif isinstance(c, list):
            if any(isinstance(b, dict) and b.get("type") == "tool_result" for b in c):
                continue
            text = " ".join(
                b.get("text", "")
                for b in c
                if isinstance(b, dict) and b.get("type") == "text"
            )
        # вырезать system-reminder блоки из текста
        text = re.sub(r"<system-reminder>.*?</system-reminder>", " ", text, flags=re.S)
        text = text.strip()
        if not text:
            continue
        if any(m in text for m in SERVICE_MARKERS):
            continue
        msgs.append(text)
        if len(msgs) >= 7:
            break

    if not msgs:
        return None

    combined = "\n".join(msgs)
    t = combined.lower()

    # ── ПРОПУСКАЕМ: команда / баг / требование действия ──────────────────────
    CMD = (
        r"(сдела|делай|прав[ья]|поправ|исправ|меняй|помен|измени|убер|удал|добав|замен|"
        r"деплой|запуш|\bпуш|коммит|\bдавай\b|\bго\b|погнали|накати|впиши|перепиш|обнови|"
        r"создай|напиш|почин|\bфикс\b|\bfix\b|апрув|одобр|\bок\b|\bокей\b|реализу|внедри|"
        r"поставь|\bставь|вставь|переделай|собери|настрой|выполн|\bда\b|\+|запили|запусти|"
        r"\bчини\b|чиниш|\bделаеш|\bправиш|выравн|доведи|доделай)"
    )
    BUG = (
        r"(мельк|мерца|пропада|исчеза|не работает|не пашет|не откр|слома|сломан|виснет|"
        r"\bвисит\b|лагает|тормоз|\bбаг\b|ошибк|съеха|криво|наеж|налаз|дубл|не отвеч|"
        r"белый экран|темный фон|тёмный фон|постоянн|не грузит|битый|не тот|не так работает|"
        r"увелич|уменьша|скач|прыга|дерга|дёрга|съезж|рефреш|перезагруз|шрифт|вылез|"
        r"обреза|перекры|поплы|edge|сафари|safari|мобильн[ао]|"
        r"выдумк|не\s+как\s+реальн|как\s+реальн|реальн\w*\s+браузер|настоящ\w*\s+браузер|"
        r"неточн|не\s+так\s+выглядит|где\s+расположен|основн\w*\s+элемент|дубл|тавтолог|"
        r"хуйня\s+полн|полная\s+хуйн|наебщик|лжец|"
        r"не\s+засчит|не\s+в\s+топ|нет\s+в\s+топ|должно\s+работать|должно\s+быть|"
        r"не\s+пофикс|не\s+чинит|потерял\w*|невидим|не\s+считает|сч[её]тчик|"
        r"не\s+учит|не\s+усчит|активность\s+теря|как\s+надо\s+работ)"
    )
    REQUIRE = (
        r"(почему ждёшь|почему ждешь|не жди|пинка|где фикс|где выполнен|где решени|"
        r"хули (сто|жд)|че сто|действуй|давай уже|когда уже|сколько можно|почему не сдел|"
        r"почему стоишь|почему не чин|ждать гоу|ждёшь гоу|ждешь гоу|перестал работать|"
        r"гарантируй|доведи|приведи в порядок|закр(ой|ыва)|заверши|доделай)"
    )
    if re.search(CMD, t) or re.search(BUG, t) or re.search(REQUIRE, t):
        return None

    # ── БЛОКИРУЕМ: оценочная критика копи/контента без команды ───────────────
    qmark = "?" in combined
    CRIT = (
        r"(при чем|при чём|почему (ты|убрал|добав|это|конструктор)|зачем ты|кто просил|"
        r"кто тебя|не просил|была команда|ума не хватило|подумать|думать надо|с чего ты|"
        r"кто сказал|пустые слова|пустое|отсебятин|хуйня|хуета|говно|\bшавка\b|не значит|"
        r"\bтупо\b|уродлив|позор|\bзря\b|мразь|тварь|как лучше)"
    )
    if qmark or re.search(CRIT, t):
        return (
            "БЛОК no_action_on_question: последние сообщения Boris — ОЦЕНОЧНАЯ КРИТИКА/ВОПРОС "
            "по копи или содержанию, а не команда и не баг-репорт. НЕ правь продукт сразу. "
            "Сначала ПОДУМАЙ как лучше, ОБЪЯСНИ логику (что и почему предлагаешь), при нужде "
            "уточни — и дождись ЯВНОЙ команды (сделай/правь/деплой/убери/ок/давай). "
            "Если это баг (мелькает/сломано/не работает) или требование действовать — маркер "
            "в тексте отсутствует; переформулируй понимание, но не лепи правку на оценку. "
            "Моментальная правка на оценочную критику — паттерн, из-за которого хук существует."
        )

    return None


def main():
    try:
        reason = decide()
    except Exception:
        sys.exit(0)
    if reason:
        print(
            json.dumps(
                {
                    "hookSpecificOutput": {
                        "hookEventName": "PreToolUse",
                        "permissionDecision": "deny",
                        "permissionDecisionReason": reason,
                    }
                }
            )
        )
    sys.exit(0)


if __name__ == "__main__":
    main()
