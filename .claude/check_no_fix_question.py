"""Stop hook — блокирует ЛЮБОЙ вопрос-разрешение на действие в конце ответа.

Правило: вижу баг / сломанное / возможность улучшить → делаю молча → отчитываюсь.
НИКОГДА не спрашивать 'делать?', 'чинить?', 'фиксить?', 'править?', 'оформить?',
'запускать?', 'добавить?', 'сделать?' и прочие вопросики-разрешения.

Логика: берем последнюю фразу-вопрос ответа. Если она заканчивается на '?' И содержит
глагол-действие (инфинитив/предложение сделать) — блокируем. Не завязано на слово 'баг'.
"""
import json
import re
import sys
from pathlib import Path


# Глаголы-действия (инфинитивы и формы предложения) — все варианты «сделать ли X».
# Включают и глаголы ПРАВКИ, и глаголы ПРОВЕРКИ (проверю/гляну/сверю/посмотрю/почитаю) —
# спрашивать разрешение на проверку запрещено так же, как на правку.
ACTION_VERB_RE = re.compile(
    r"\b("
    r"дела(?:ть|ю|ем)|сдела(?:ть|ю|ем)|"
    r"фикс\w*|чин(?:ить|ю)|почин\w*|исправ\w*|"
    r"прав(?:ить|лю|ка|ки)|поправ\w*|"
    r"добав\w*|мен(?:ять|яю)|помен\w*|измен\w*|"
    r"закр(?:ыть|ою)|обнов\w*|внес\w*|примен\w*|перепис\w*|"
    r"оформ\w*|запуск\w*|запуст\w*|запущу|"
    r"став(?:ить|лю)|постав\w*|убра(?:ть|ю)|убер\w*|удал\w*|замен\w*|"
    r"деплой\w*|задеплои\w*|пуш\w*|запуш\w*|"
    r"да(?:ть|вать)|вкл(?:ючить|ючать)|подключ\w*|настро\w*|"
    r"созда(?:ть|ю)|собра(?:ть|ю)|пересобра\w*|переоформ\w*|"
    r"коммит\w*|закоммит\w*|откатить|вернуть|"
    # глаголы проверки/чтения — спрашивать «проверить?/глянуть?» тоже запрещено
    r"провер(?:ить|ю|яю)|перепровер\w*|"
    r"посмотр(?:еть|ю)|смотр(?:еть|ю)|гля(?:нуть|ну|жу|деть)|глян\w*|загля\w*|"
    r"свер(?:ить|ю|яю)|прове\w*|чек\w*|"
    r"чита(?:ть|ю)|почита(?:ть|ю)|прочита(?:ть|ю)|"
    r"иска(?:ть)|ищу|поиск\w*|поищ\w*|найд\w*|"
    r"показа(?:ть)|покаж\w*|изуч\w*|разбер\w*|разобра\w*|"
    r"диагност\w*|проанализ\w*|анализ\w*|погру\w*|копн\w*|копа\w*"
    r")\b",
    re.IGNORECASE | re.UNICODE,
)

# Короткие сигналы-разрешения без явного глагола: «Сказать?», «Продолжать?», «Начинать?».
# Ими я маскирую спрашивание, когда действие названо в предыдущем утвердит. предложении.
PERMISSION_SIGNAL_RE = re.compile(
    r"\b("
    r"сказать|продолж\w*|начина(?:ть)|начать|начну|начина(?:ю|ем)|"
    r"дальше|погнал\w*|поехал\w*|поган\w*|старту\w*|го|окей|ок"
    r")\b",
    re.IGNORECASE | re.UNICODE,
)

# Извлечь все фразы-вопросы (текст, заканчивающийся на ?)
QUESTION_CLAUSE_RE = re.compile(r"[^.!?\n]*\?", re.UNICODE)

# Клянчить разрешение БЕЗ вопросительного знака: «Скажешь "делай" — внесу»,
# «жду команды», «по команде сделаю», «дашь добро — починю». Это тот же промах,
# что вопрос-разрешение, только замаскированный под утверждение. Ловим в хвосте
# ответа независимо от финального знака препинания.
BEG_PERMISSION_RE = re.compile(
    r"("
    r"скаж(?:и|ешь)\s+[«\"']?\s*(?:делай|ок|давай|го|поехали)|"
    r"скаж(?:и|ешь)\b[^.!?\n]{0,50}\b(?:сделаю|внес\w*|поправ\w*|почин\w*|исправ\w*|добав\w*|замен\w*|запуш\w*|задеплои\w*|обнов\w*|настро\w*|вкл\w*)|"
    r"жду\s+(?:тво(?:ей|его)\s+)?команд\w*|"
    r"жд[её]т\s+(?:тво(?:ей|его)\s+)?команд\w*|"
    r"по\s+(?:тво(?:ей|ему)\s+)?команде\b[^.!?\n]{0,40}\b(?:сделаю|внес\w*|поправ\w*|почин\w*|исправ\w*|добав\w*|замен\w*|запуш\w*|задеплои\w*|обнов\w*|настро\w*)|"
    r"даш[ьъ]?\s+(?:команд\w*|добро|отмашк\w*|зелен\w*)|"
    r"готов\s+[^.!?\n]{0,40}\bпо\s+команде|"
    r"на\s+тво[юёе]\s+команд\w*|"
    r"как\s+скажешь\s*[—-]\s*(?:сделаю|внес\w*|поправ\w*|почин\w*|добав\w*)"
    r")",
    re.IGNORECASE | re.UNICODE,
)


def _get_text(content) -> str:
    if isinstance(content, str):
        return content
    if isinstance(content, list):
        parts = []
        for block in content:
            if isinstance(block, dict) and block.get("type") == "text":
                parts.append(block.get("text", ""))
        return " ".join(parts)
    return ""


def _is_tool_result_message(msg: dict) -> bool:
    content = msg.get("message", {}).get("content", [])
    if not isinstance(content, list) or not content:
        return False
    return all(b.get("type") == "tool_result" for b in content)


def load_response(transcript_path: str) -> str:
    """Последний НЕПУСТОЙ text-блок assistant по ВСЕМУ транскрипту.

    НЕ завязано на «последнего human»: Boris пишет mid-turn, его реплики попадают
    в транскрипт ПОСЛЕ моего вопроса-разрешения и сдвигают точку отсчёта — старая
    логика (всё после last_human) теряла мой «...сказать?» и хук молчал. Берём мой
    последний текст как есть — он и есть финальная реплика, которую видит Boris.
    """
    p = Path(transcript_path)
    if not p.exists():
        return ""

    messages = []
    for line in p.read_text(encoding="utf-8", errors="ignore").splitlines():
        try:
            messages.append(json.loads(line))
        except Exception:
            continue

    last_text = ""
    for msg in messages:
        if msg.get("type") != "assistant":
            continue
        t = _get_text(msg.get("message", {}).get("content", []))
        if t.strip():
            last_text = t
    return last_text


def is_permission_question(response: str) -> bool:
    """True, если ответ заканчивается вопросом-разрешением на действие.

    Ловит две формы:
      1. Глагол-действие внутри самого вопроса: «Починить?», «Проверить sitemap?».
      2. Действие названо утвердит. предложением, а разрешение просит короткий
         вопрос-сигнал: «Сам проверю sitemap. Сказать?» / «... Продолжать?».
    Поэтому смотрим не только текст вопроса, а весь ХВОСТ ответа (последние ~220
    символов), где сидит и утвердит. предложение с глаголом, и короткий вопрос.
    """
    text = response.strip()
    if not text:
        return False
    tail = text[-260:]
    # Форма БЕЗ вопросительного знака: «Скажешь делай — внесу», «жду команды»,
    # «по команде сделаю», «дашь добро — починю». Клянчить разрешение утверждением.
    if BEG_PERMISSION_RE.search(tail):
        return True
    # ответ заканчивается вопросом-разрешением на действие
    if not text.rstrip().endswith("?"):
        return False
    # в хвосте есть глагол-действие/проверки ИЛИ короткий сигнал-разрешение → блок
    return bool(ACTION_VERB_RE.search(tail) or PERMISSION_SIGNAL_RE.search(tail))


def main():
    try:
        payload = json.load(sys.stdin)
    except Exception:
        sys.exit(0)

    if payload.get("stop_hook_active"):
        sys.exit(0)

    transcript_path = payload.get("transcript_path")
    if not transcript_path:
        sys.exit(0)

    response = load_response(transcript_path)
    if not response.strip():
        sys.exit(0)

    # БЛОКИРУЕМ вопрос-разрешение. Срабатывает только когда я реально спросил
    # «чинить?/править?/деплоить?» — то есть в момент промаха, не постоянно.
    # Источник массовых дублей (ложные срабатывания на упоминание файла) убран
    # отдельно в check_sources.py, поэтому блокировку можно безопасно вернуть.
    if is_permission_question(response):
        reason = (
            "НАРУШЕНИЕ: в конце ответа вопрос-разрешение на действие "
            "('чинить?/править?/сделать?/деплоить?/добавить?' и т.п.). ЗАПРЕЩЕНО. "
            "Вижу баг или возможность → молча делаю → отчитываюсь о сделанном. "
            "Убрать вопрос полностью: либо выполнить действие прямо сейчас (Edit/Bash) "
            "и отчитаться, либо просто констатировать без спрашивания разрешения."
        )
        print(json.dumps({"decision": "block", "reason": reason}))
        sys.exit(0)

    sys.exit(0)


if __name__ == "__main__":
    main()
