/**
 * Удаляет секцию «ВОЗМОЖНЫЕ АПСЕЙЛЫ:» из ответа калькулятора.
 * Используется при отправке клиенту (email, PDF, страница калькулятора) —
 * апсейлы это внутренняя информация для владельца агентства, клиенту видеть не надо.
 *
 * В Telegram-уведомлении владельцу применять НЕ нужно — там reply передается полным.
 */
export function stripUpsells(reply: string): string {
  return reply.replace(
    /\n*ВОЗМОЖНЫЕ АПСЕЙЛЫ:[\s\S]*?(?=\nПРИМЕЧАНИЕ:|$)/g,
    "",
  );
}
